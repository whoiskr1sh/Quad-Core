                <h2 style="color: white;">What We Do</h2>
<h4 style="color: white;">1. Food Donation Programs</h4>
<p>Our primary focus is on collecting and distributing food to those in need. Our programs include:<br>

<b>Emergency Food Assistance:</b> We provide immediate support to individuals and families facing urgent food shortages. This includes pre-packaged food boxes and hot meal distributions.

<b>Community Pantries:</b> We operate local food pantries that offer a variety of essential items, allowing individuals and families to select what they need most.

<b>School Meal Programs:</b> We partner with schools to ensure children receive healthy meals during the school day, as well as during school breaks.
</p>

<h4 style="color: white;">2. Food Recovery and Redistribution</h4>
<p>To minimize food waste and maximize our impact, we work closely with local businesses, restaurants, and farms to recover surplus food. This recovered food is then redistributed through our network of community partners and directly to those in need.</p>

<h4 style="color: white;">3. Nutritional Education</h4>
<p>Understanding that access to food is just one part of the solution, we provide educational programs that teach healthy eating habits, meal planning, and cooking skills. Our goal is to empower individuals and families to make informed food choices and improve their overall well-being.</p>

